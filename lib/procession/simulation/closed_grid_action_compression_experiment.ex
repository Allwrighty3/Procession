defmodule Procession.Simulation.ClosedGridActionCompressionExperiment do
  @moduledoc """
  A 4x4 embodied world with explicit movement, consumption, and recovery actions.

  Each chosen action emits a short internal event sequence into terrain-owned natural
  compression discovery. The compressor receives only activity generated by the world,
  never a predeclared behavior trace or semantic routine boundary.
  """

  alias Procession.Simulation.RelationalTerrainNaturalCompression, as: Compression

  @directions [:north, :south, :east, :west]
  @actions @directions ++ [:consume, :rest]

  defmodule Resource do
    @moduledoc false
    defstruct [:id, :position, :capacity, :amount, :regen]
  end

  defmodule State do
    @moduledoc false
    defstruct tick: 0,
              position: {1, 1},
              energy: 0.62,
              fatigue: 0.0,
              resources: [],
              compression: nil,
              action_counts: %{},
              event_history: [],
              history: [],
              alive: true,
              intake: 0.0
  end

  def actions, do: @actions

  def default_resources do
    [
      %Resource{id: :spring, position: {0, 0}, capacity: 0.55, amount: 0.55, regen: 0.018},
      %Resource{id: :grove, position: {3, 0}, capacity: 0.42, amount: 0.42, regen: 0.014},
      %Resource{id: :cache, position: {2, 3}, capacity: 0.65, amount: 0.65, regen: 0.010}
    ]
  end

  def run(opts \\ []) do
    ticks = Keyword.get(opts, :ticks, 320)
    compression_opts = compression_opts(opts)

    initial = %State{
      position: Keyword.get(opts, :initial_position, {1, 1}),
      energy: Keyword.get(opts, :initial_energy, 0.62),
      resources: Keyword.get(opts, :resources, default_resources()),
      compression: Compression.new(compression_opts),
      action_counts: Map.new(@actions, &{&1, 0})
    }

    Enum.reduce_while(1..ticks, initial, fn tick, state ->
      next = advance(state, tick, opts, compression_opts)
      if next.alive, do: {:cont, next}, else: {:halt, next}
    end)
  end

  def instrumentation(%State{} = state) do
    base = Compression.instrumentation(state.compression)
    plan = Compression.compression_plan(state.compression, Enum.reverse(state.event_history))

    Map.merge(base, %{
      detailed_event_transitions: plan.detailed_transitions,
      effective_event_transitions: plan.effective_transitions,
      transitions_saved: plan.transitions_saved,
      compression_ratio: plan.compression_ratio,
      action_counts: state.action_counts,
      event_count: length(state.event_history)
    })
  end

  def assemblies(%State{} = state), do: Compression.assemblies(state.compression)

  defp advance(state, tick, opts, compression_opts) do
    resources = regenerate(state.resources)
    base_energy = state.energy - Keyword.get(opts, :metabolic_cost, 0.010)
    hunger = clamp(1.0 - base_energy)
    gradients = gradients(state.position, resources, Keyword.get(opts, :perception_range, 3))
    at_resource = resource_at(resources, state.position)
    scores = action_scores(state, hunger, gradients, at_resource, tick, opts)
    action = scores |> Enum.max_by(fn {_action, score} -> score end) |> elem(0)
    {position, resources, intake, outcome} = execute(action, state.position, resources, hunger, opts)
    moved? = position != state.position
    fatigue = update_fatigue(state.fatigue, action, moved?, opts)
    move_cost = if moved?, do: Keyword.get(opts, :movement_cost, 0.018), else: 0.0
    energy = clamp(base_energy - move_cost + intake)

    events = internal_events(action, hunger, gradients, at_resource, outcome)
    compression = Enum.reduce(events, state.compression, &Compression.observe(&2, &1, compression_opts))

    %{state |
      tick: tick,
      position: position,
      energy: energy,
      fatigue: fatigue,
      resources: resources,
      compression: compression,
      alive: energy > 0.0,
      intake: state.intake + intake,
      action_counts: Map.update!(state.action_counts, action, &(&1 + 1)),
      event_history: Enum.reverse(events) ++ state.event_history,
      history: [%{tick: tick, action: action, outcome: outcome, position: position,
        energy: energy, fatigue: fatigue, intake: intake, scores: scores} | state.history]
    }
  end

  defp action_scores(state, hunger, gradients, at_resource, tick, opts) do
    noise = Keyword.get(opts, :fluctuation_magnitude, 0.008)

    movement =
      Map.new(@directions, fn direction ->
        blocked = step(state.position, direction) == state.position
        score = Map.fetch!(gradients, direction) * hunger - state.fatigue * 0.40 - bool_value(blocked) * 0.30 +
          centered({tick, direction}) * noise
        {direction, score}
      end)

    consume = if at_resource && at_resource.amount > 0.01, do: hunger * 1.20 + at_resource.amount * 0.30, else: -0.50
    rest = state.fatigue * 0.95 + (1.0 - hunger) * 0.12
    movement |> Map.put(:consume, consume) |> Map.put(:rest, rest)
  end

  defp internal_events(action, hunger, gradients, at_resource, outcome) do
    sensed =
      cond do
        at_resource && at_resource.amount > 0.01 -> {:sense, :resource_here}
        Enum.max(Map.values(gradients), fn -> 0.0 end) > 0.0 -> {:sense, :resource_gradient}
        true -> {:sense, :no_resource_signal}
      end

    family = if action in @directions, do: :locomotion, else: action
    need = if hunger >= 0.55, do: :high_need, else: :low_need

    [
      sensed,
      {:need, need},
      {:recruit, family},
      {:execute, action},
      {:consequence, outcome}
    ]
  end

  defp execute(:consume, position, resources, hunger, opts) do
    bite = Keyword.get(opts, :bite_size, 0.16) * hunger

    {resources, intake} =
      Enum.map_reduce(resources, 0.0, fn resource, total ->
        if resource.position == position and resource.amount > 0.0 do
          taken = min(resource.amount, bite)
          {%{resource | amount: resource.amount - taken}, total + taken}
        else
          {resource, total}
        end
      end)

    outcome = if intake > 0.0, do: :intake, else: :empty_consume
    {position, resources, intake, outcome}
  end

  defp execute(:rest, position, resources, _hunger, _opts), do: {position, resources, 0.0, :recovered}

  defp execute(direction, position, resources, _hunger, _opts) when direction in @directions do
    next = step(position, direction)
    outcome = if next == position, do: :blocked, else: :displaced
    {next, resources, 0.0, outcome}
  end

  defp update_fatigue(value, :rest, _moved?, opts),
    do: max(0.0, value - Keyword.get(opts, :rest_recovery, 0.075))
  defp update_fatigue(value, :consume, _moved?, opts),
    do: max(0.0, value - Keyword.get(opts, :consume_recovery, 0.025))
  defp update_fatigue(value, _action, true, opts),
    do: min(1.0, value + Keyword.get(opts, :movement_fatigue, 0.050))
  defp update_fatigue(value, _action, false, _opts), do: value

  defp compression_opts(opts) do
    Keyword.merge([
      dimensions: 8,
      auto_expand_dimensions: false,
      reuse_radius: 0.001,
      compression_window: 16,
      assembly_occurrence_thresholds: %{4 => 10, 8 => 24, 16 => 60},
      encoding_salt: :closed_grid_action_compression
    ], Keyword.get(opts, :compression_opts, []))
  end

  defp gradients(position, resources, range) do
    Enum.reduce(resources, Map.new(@directions, &{&1, 0.0}), fn resource, acc ->
      distance = manhattan(position, resource.position)

      if distance <= range and resource.amount > 0.001 do
        strength = resource.amount / max(distance, 1)
        Enum.reduce(@directions, acc, fn direction, values ->
          improvement = distance - manhattan(step(position, direction), resource.position)
          if improvement > 0, do: Map.update!(values, direction, &(&1 + strength)), else: values
        end)
      else
        acc
      end
    end)
  end

  defp resource_at(resources, position), do: Enum.find(resources, &(&1.position == position))

  defp regenerate(resources), do: Enum.map(resources, fn resource ->
    %{resource | amount: min(resource.capacity, resource.amount + resource.regen)}
  end)

  defp step({x, y}, :north), do: {x, max(0, y - 1)}
  defp step({x, y}, :south), do: {x, min(3, y + 1)}
  defp step({x, y}, :east), do: {min(3, x + 1), y}
  defp step({x, y}, :west), do: {max(0, x - 1), y}

  defp manhattan({x1, y1}, {x2, y2}), do: abs(x1 - x2) + abs(y1 - y2)
  defp bool_value(true), do: 1.0
  defp bool_value(false), do: 0.0
  defp clamp(value), do: value |> max(0.0) |> min(1.0)
  defp centered(seed), do: :erlang.phash2(seed, 1_000_000) / 500_000 - 1.0
end
